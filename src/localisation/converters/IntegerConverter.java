package localisation.converters;

import localisation.StringConverter;

/**
 * Konvertor {@code String} objekta u {@code java.lang.Integer} objekat.
 * 
 * @author danijel
 * @version 1.0, 15.11.2015.
 * @since 1.0
 * @see java.lang.Integer#parseInt(String)
 *
 */
public class IntegerConverter implements StringConverter<Integer>{

	@Override
	public Integer convert(String s) {
		return Integer.parseInt(s);
	}

	

}
