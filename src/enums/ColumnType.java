package enums;

/**
 * Ova enumeracija modeluje tipove kolona unutar tabela.<br/>
 * Moguce enumeracije su:<br/>
 *	- {@code NUMERIC} tip sadrzaja unutar kolone je numericki (npr: int, longint...);<br/>
 *	- {@code DECIMAL} tip sadrzaja unutar kolone je numericki sa decimalnim zarezom (npr: double, float...);<br/>
 *	- {@code DATE} tip sadrzaja unutar kolone je datumski;<br/>
 *	- {@code DATETIME} tip sadrzaja unutar kolone je datumsko-vremenski;<br/>
 *	- {@code BOOLEAN} tip sadrzaja unutar kolone je istinitosna vrednost;<br/>
 *	- {@code ALPHABET} tip sadrzaja unutar kolone je karakter, ili niz karaktera (npr: varchar, char...).
 * @author NemanjaM
 *
 */
public enum ColumnType {
	
	NUMERIC,
	DECIMAL,
	DATE,
	DATETIME,
	BOOLEAN,
	ALPHABET
	
}
